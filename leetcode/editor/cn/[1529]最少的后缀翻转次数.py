class Solution:
    def minFlips(self, target: str) -> int:
        while target.startswith("0"):
            target = target[1:]
        if target == '':
            return 0

        tem = target[0]
        for i in range(1, len(target)):
            if target[i] != target[i - 1]:
                tem = tem + target[i]
        return len(tem)
